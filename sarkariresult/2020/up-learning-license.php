<li>After that fees have to be paid the UP Learning License Fees.</li>
<li>Then you have to select the slot where the learning test is to be held.</li>
<li>After booking the slot, print out all Document (Receipt / Online Form / Form)</li>
<li>Now you have to take the exam by going to the respective RTO office on the day you book the Slot for UP Learning License.</li>
<li>You will get your result immediately after the exam</li>
<li>Now after a few days you can download your learning license online</li>
